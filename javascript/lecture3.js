//DATA TYPES
//String
var x="helllo";
console.log(x);
//console.log("<br>");
console.log(typeof x);
//output:string type
//helllo

//number
var y=89;

console.log(y);

console.log(typeof y);
//output:number
//89


//boolean
var y=true;
console.log(y);

console.log(typeof y);
//output:boolean
//true



// array
var y=["html","css","js"];
console.log(y);

console.log(typeof y);
//output:(3) ['html', 'css', 'js']
//object


//object
var y={first:"name",last:"name"};
console.log(y);


console.log(typeof y);
//output:{first: 'name', last: 'name'}
//object


// Undefined
var z;
console.log(z);

console.log(typeof z);
//output:undefined


// Undefined
var z=null;
console.log(z);

console.log(typeof z);
//output:undefined